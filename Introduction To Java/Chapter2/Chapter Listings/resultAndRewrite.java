public class resultAndRewrite {

    /**
     * @param args
     */
    public static void main(String[] args) {

        //Establish Variables
        int original = 25 / 4;
        float newOriginal = 25 / 4;

        // First show the result of 25 / 4 
        System.out.println("25 / 4 is: " + original);

        // Show the result of a floating point number
        System.out.println("25 / 4 as a floating point number is " + newOriginal);
    }
}
