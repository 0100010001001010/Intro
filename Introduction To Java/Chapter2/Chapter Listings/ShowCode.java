/* 
 * Show the output of the following code:
                int a = 6;
                int b = a++;
                System.out.println(a);
                System.out.println(b);
                a = 6;
                b = ++a;
                System.out.println(a);
                System.out.println(b);
 */

public class ShowCode {

    public static void main(String[] args) {
        int a = 6;
        int b = a++;
        System.out.println(a);
        System.out.println(b);
        a = 6;
        b = ++a;
        System.out.println(a);
        System.out.println(b);
    }
}
